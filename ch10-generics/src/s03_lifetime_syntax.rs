pub fn section_3() {
    {
        let r: u32;           // ---------+-- 'a
                              //          |
        {                     //          |
            let x: u32 = 5;   // -+-- 'b  |
            r = &x;           //  |       |
        }                     // -+       |
                              //          |
        println!("r: {}", r); //          |
    }

    {
        let x: u8 = 5;        // ----------+-- 'b
                              //           |
        let r: &u8 = &x;      // --+-- 'a  |
                              //   |       |
        println!("r: {}", r); //   |       |
                              // --+       |
    }
}
